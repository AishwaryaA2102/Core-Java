package com.java.daytwo.Looping;

public class Arraysinforloop {

	public static void main(String[] args) {
		int a[] = {10,20,30,40,50};
		//for integer array
		for (int i:a)
		{
			System.out.println(i);
		}
		System.out.println();
		//for character array
		char ch[] = {'j','a','v','a'};
		for (char i:ch)
		{
			System.out.println(i);
		}

	}

}
